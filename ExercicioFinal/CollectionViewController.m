#pragma mark <UICollectionViewDataSource>

- (NSInteger)numberOfSectionsInCollectionView:(UICollectionView *)collectionView {
    return 1;
}


- (NSInteger)collectionView:(UICollectionView *)collectionView numberOfItemsInSection:(NSInteger)section {
    return self.imagens.count;
}

- (UICollectionViewCell *)collectionView:(UICollectionView *)collectionView cellForItemAtIndexPath:(NSIndexPath *)indexPath {
    
    UICollectionViewCell *cell = [collectionView dequeueReusableCellWithReuseIdentifier:reuseIdentifier forIndexPath:indexPath];
    
    // Configure the cell
    
    UIImageView *imagemView = (UIImageView *)[cell viewWithTag:100];
    NSString *nomeImagem = [self.imagens objectAtIndex:indexPath.row];
    
    imagemView.image = [UIImage imageNamed:nomeImagem];
    
    [self.view addSubview:imagemView];
    
    cell.backgroundColor=[UIColor colorWithPatternImage:[UIImage imageNamed:[self.imagens objectAtIndex:indexPath.row]]];
    
    return cell;
}

